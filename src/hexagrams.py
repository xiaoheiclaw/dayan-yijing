YAO_LINES = {
    6: "▅▅ ✕ ▅▅",  # 老阴 → 变阳
    7: "▅▅▅▅▅▅▅",  # 少阳
    8: "▅▅  ▅▅",    # 少阴
    9: "▅▅▅▅▅▅▅ ○", # 老阳 → 变阴
}

YAO_NAMES = ["初", "二", "三", "四", "五", "上"]

def yao_to_bit(yao: int) -> int:
    return 1 if yao in (7, 9) else 0

def is_changing(yao: int) -> bool:
    return yao in (6, 9)

def changed_bit(yao: int) -> int:
    if yao == 6:
        return 1
    if yao == 9:
        return 0
    return yao_to_bit(yao)

def lookup_hexagram(yaos: list[int]) -> dict:
    lower_bits = tuple(yao_to_bit(y) for y in yaos[:3])
    upper_bits = tuple(yao_to_bit(y) for y in yaos[3:])
    lower = TRIGRAM_NAMES[lower_bits]
    upper = TRIGRAM_NAMES[upper_bits]
    seq, name, ci = HEXAGRAMS[(upper, lower)]

    changing = [i for i, y in enumerate(yaos) if is_changing(y)]

    result = {
        "seq": seq,
        "name": name,
        "ci": ci,
        "upper": upper,
        "lower": lower,
        "upper_nature": TRIGRAM_NATURE[upper],
        "lower_nature": TRIGRAM_NATURE[lower],
        "upper_symbol": TRIGRAM_SYMBOLS[upper],
        "lower_symbol": TRIGRAM_SYMBOLS[lower],
        "yaos": yaos,
        "changing": changing,
    }

    if changing:
        changed_lower = tuple(changed_bit(y) for y in yaos[:3])
        changed_upper = tuple(changed_bit(y) for y in yaos[3:])
        cl = TRIGRAM_NAMES[changed_lower]
        cu = TRIGRAM_NAMES[changed_upper]
        s2, n2, c2 = HEXAGRAMS[(cu, cl)]
        result["zhi_gua"] = {
            "seq": s2,
            "name": n2,
            "ci": c2,
            "upper": cu,
            "lower": cl,
        }

    return result
